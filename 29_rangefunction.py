# used to generate a sequence of characters
# also specify the start,stop and step sise
for i in range(8): #prints 0 to 7
    print(i)

for i in range(1,8): #here 1 indicates that the series start from 1 and stop at 8
    print(i)         #prints 1 to 7

for i in range(1,8,2): #last argument pass as step size
    print(i)           #prints 1,3,5,7  
    
       